package dataType;

public class DataType {

	public static void main(String[] args) {
		byte var_byte = 1;
		System.out.println(var_byte);

		char var_char_1 = 'A'; // 문자를 직접 저장
		char var_char_2 = 65; // 10진수로 저장
		char var_char_3 = '\u0041'; // 16진수로 저장

		System.out.println(var_char_1);
		System.out.println(var_char_2);
		System.out.println(var_char_3);

	}

}
